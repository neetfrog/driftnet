export function filterSites(
  sites: DriftSite[],
  mood: string,
  decade: string,
  topic: string,
  searchQuery: string
): DriftSite[] {
  return sites.filter((site) => {
    const moodMatch = mood === "all" || site.mood.includes(mood);
    const decadeMatch = decade === "all" || site.decade === decade;
    const topicMatch = topic === "" || site.topics.includes(topic);
    const searchMatch =
      searchQuery === "" ||
      site.title.toLowerCase().includes(searchQuery.toLowerCase()) ||
      site.description.toLowerCase().includes(searchQuery.toLowerCase()) ||
      site.topics.some((t) => t.toLowerCase().includes(searchQuery.toLowerCase()));
    return moodMatch && decadeMatch && topicMatch && searchMatch;
  });
}

export function getRandomSite(
  sites: DriftSite[],
  mood: string,
  decade: string
): DriftSite {
  const filtered = sites.filter((site) => {
    const moodMatch = mood === "all" || site.mood.includes(mood);
    const decadeMatch = decade === "all" || site.decade === decade;
    return moodMatch && decadeMatch;
  });
  const pool = filtered.length > 0 ? filtered : sites;
  return pool[Math.floor(Math.random() * pool.length)];
}
